# Алгоритм:
# 1. Преобразовать строку к одному регистру, чтобы исключить чувствительность к регистру.
# 2. Удалить из строки все символы, кроме букв и цифр, чтобы учитывать только значимые символы.
# 3. Сравнить очищенную строку с её перевёрнутой версией.
# 4. Если очищенная строка совпадает с перевёрнутой, то это палиндром; иначе — нет.

def is_palindrome(s):
    # Преобразуем строку к нижнему регистру
    s = s.lower()
    # Удаляем все символы, кроме букв и цифр
    s = ''.join(char for char in s if char.isalnum())
    # Сравниваем строку с её перевёрнутой версией

    # Сравниваем строку с её перевёрнутой версией
    if s == s[::-1]:
        print(f"'{s}' — это палиндром.")
    else:
        print(f"'{s}' — это не палиндром.")

# Пример использования:
is_palindrome("Шалаш")  # True
is_palindrome("кабан")    # False